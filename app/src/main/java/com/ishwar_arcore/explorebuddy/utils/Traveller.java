package com.ishwar_arcore.explorebuddy.utils;

public class Traveller {

    private String addressone;
    private String addresstwo;
    private String age;
    private String city;
    private String email;
    private String phonenumber;
    private String pincode;
    private String profile;
    private String gender;
    private String state;
    private String id;
    private String name;

    public Traveller() {
    }

    public Traveller(String addressone, String addresstwo, String age, String city, String email, String phonenumber, String pincode, String profile, String gender, String state, String id, String name) {
        this.addressone = addressone;
        this.addresstwo = addresstwo;
        this.age = age;
        this.city = city;
        this.email = email;
        this.phonenumber = phonenumber;
        this.pincode = pincode;
        this.profile = profile;
        this.gender = gender;
        this.state = state;
        this.id = id;
        this.name = name;
    }

    public String getAddressone() {
        return addressone;
    }

    public void setAddressone(String addressone) {
        this.addressone = addressone;
    }

    public String getAddresstwo() {
        return addresstwo;
    }

    public void setAddresstwo(String addresstwo) {
        this.addresstwo = addresstwo;
    }

    public String getAge() {
        return age;
    }

    public void setAge(String age) {
        this.age = age;
    }

    public String getCity() {
        return city;
    }

    public void setCity(String city) {
        this.city = city;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getPhonenumber() {
        return phonenumber;
    }

    public void setPhonenumber(String phonenumber) {
        this.phonenumber = phonenumber;
    }

    public String getPincode() {
        return pincode;
    }

    public void setPincode(String pincode) {
        this.pincode = pincode;
    }

    public String getProfile() {
        return profile;
    }

    public void setProfile(String profile) {
        this.profile = profile;
    }

    public String getGender() {
        return gender;
    }

    public void setGender(String gender) {
        this.gender = gender;
    }

    public String getState() {
        return state;
    }

    public void setState(String state) {
        this.state = state;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
